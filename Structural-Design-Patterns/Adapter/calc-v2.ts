export default class Calculator {
  add(num1: number, num2: number): number {
    return num1 + num2;
  }

  subtract(num1: number, num2: number): number {
    return num1 - num2;
  }

  division(num1: number, num2: number): number {
    return num1 / num2;
  }

  multiplication(num1: number, num2: number): number {
    return num1 * num2;
  }
}
